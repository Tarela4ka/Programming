signed main(){
    ios::sync_with_stdio(false); 
    cin.tie(NULL); cout.tie(NULL);
    #ifdef DEBUG
    clock_t t1,t2;
    t1=clock();
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);
    #endif
    
    int t; cin >> t;
    while(t--){
        int n, k; cin >> n >> k;
        int tn = n; int ans = 0; int c = 1;
        while(tn >= k){
            if (tn&1) ans += c*(tn/2+1) + tn*(c*c-c)/2 + (n%c)*c/2;
            c <<= 1; tn >>= 1; 
        }
        cout << ans << endl;
    }

    #ifdef DEBUG
    t2=clock();
    float diff ((float)t2-(float)t1);
    cout << '\n' << diff << "ms\n";
    #endif
    return 0;
}
