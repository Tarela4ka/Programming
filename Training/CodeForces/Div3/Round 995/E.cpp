using v = vector<T>;

const int MOD = 1000000007;
const int INF = 1e9;

signed main(){
    ios::sync_with_stdio(false); 
    cin.tie(NULL); cout.tie(NULL);
    #ifdef DEBUG
    clock_t t1,t2;
    t1=clock();
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);
    #endif

    int t; cin >> t;
    while(t--){
        int n, k;
        cin >> n >> k;
        v<int> a(n), b(n);
        set<int> s;
        fe(c, a) {
            cin >> c;
            s.insert(c);
        }
        fe(c, b){
            cin >> c;
            s.insert(c);
        }
        // cout << t << ": " << endl;
        sort(all(a)); sort(all(b));
        int ans = 0;
        fe(c, s){
            int x = (lower_bound(all(a), c)-a.begin());
            int pos = n-x;
            int neg = n-(lower_bound(all(b), c)-b.begin())-pos;
            // cout << c << " " << pos << " " << neg << endl; 
            if (neg <= k)
                ans = max(ans, (pos+neg)*c);
        }
        cout << ans << endl;
    }

    #ifdef DEBUG
    t2=clock();
    float diff ((float)t2-(float)t1);
    cout << '\n' << diff << "ms";
    #endif
    return 0;
}

