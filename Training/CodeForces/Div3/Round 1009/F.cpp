void solve(){
    int l1, r1, l2, r2;
    cin >> l1 >> r1 >> l2 >> r2;
    int ans = 0;
    while(true){
        int Min = min(LSOne(r1), LSOne(r2));
        if (l1 != 0) Min = min(LSOne(l1), Min);
        if (l2 != 0) Min = min(LSOne(l2), Min);
        if (LSOne(l1) == Min){
            l1 += Min;
            ans += (r2-l2)/Min;
        }
        if (LSOne(l2) == Min){
            l2 += Min;
            ans += (r1-l1)/Min;
        }
        if (LSOne(r1) == Min){
            r1 -= Min;
            ans += (r2-l2)/Min;
        }
        if (LSOne(r2) == Min){
            r2 -= Min;
            ans += (r1-l1)/Min;
        }
        if (r1-l1 == 0 || r2-l2 == 0) break; 
    }
    cout << ans << '\n';
}
 
