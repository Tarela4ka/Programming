    #endif

    cin >> n >> k;
    int s = 0;
    a.resize(n); fe(c, a) {
        cin >> c;
        s += c*2;
    }
    if (s < k){
        cout << -1;
        return 0;
    }
    int ans = P(0, 0, 0);
    if (ans == INF) cout << 0;
    else cout << ans;

    #ifdef DEBUG
    t2=clock();
    float diff ((float)t2-(float)t1);
    cout << '\n' << diff << "ms\n";
    #endif
    return 0;
