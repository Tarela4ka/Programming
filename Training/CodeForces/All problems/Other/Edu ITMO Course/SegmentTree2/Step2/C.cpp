const int MOD = 1e9 + 7;
const int neutral = (1<<30)-1;

struct segmentTree{
private:
    int size;
    struct node{
        int ans = 0, ors = 0; 
    };
    v<node> st;
    int lc(int i) { return ((i<<1)+1);}
    int rc(int i) { return ((i<<1)+2);}
    int par(int i) { return ((i-1)>>1);}
public:
    segmentTree(int n){
        size = 1;
        while(size < n) size <<= 1;
        st.resize(2*size-1);
    }
    void update(int l, int r, int x, int v, int lv, int rv){
        if (lv >= r || rv <= l) return;
        if (lv >= l && rv <= r){
            st[v].ans |= x;
            st[v].ors |= x;
            return;
        }
        int m = (lv + rv) / 2;
        update(l, r, x, lc(v), lv, m);
        update(l, r, x, rc(v), m, rv);
        st[v].ans = ((st[rc(v)].ans & st[lc(v)].ans) | st[v].ors);
    }
    void update(int l, int r, int x){
        update(l, r, x, 0, 0, size);
    }
    int req(int l, int r, int v, int lv, int rv){
        if (lv >= r || rv <= l) return neutral;
        if (lv >= l && rv <= r) return (st[v].ans | st[v].ors);
        int m = (lv + rv) / 2;
        return ((req(l, r, lc(v), lv, m) & req(l, r, rc(v), m, rv)) | st[v].ors);
    }
    int req(int l, int r){
        return req(l, r, 0, 0, size);
    }
};


signed main(){
    ios::sync_with_stdio(false); 
    cin.tie(NULL); cout.tie(NULL);
    #ifdef DEBUG
    clock_t timeStart,timeEnd;
    timeStart=clock();
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);
    #endif

    int n, q; cin >> n >> q;
    segmentTree st(n);
    while(q--){
        int z; cin >> z;
        if (z == 1){
            int l, r, v;
            cin >> l >> r >> v;
            st.update(l, r, v);
        }else{
            int l, r; cin >> l >> r;
            cout << st.req(l, r) << "\n";
        }
    }

    #ifdef DEBUG
    timeEnd=clock();
    float diff (timeEnd-timeStart);
    cout << '\n' << diff << "ms\n";
    #endif
    return 0;
}
