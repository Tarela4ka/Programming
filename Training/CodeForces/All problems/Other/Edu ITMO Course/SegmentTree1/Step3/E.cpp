using vi = v<int>;

const int MOD = 1e9+7;
const int INF = 1e10;

struct segmentTree{
private:
    int size;
    vi st;
    int lc(int i) {return ((i<<1)+1);}
    int rc(int i) {return ((i<<1)+2);}
    int par(int i) {return ((i-1)>>1);}
public:
    segmentTree (int n) {
        size = 1;
        while(size < n) size<<=1;
        st.assign(2*size-1, 0);
    }
    void add(int i){
        if (i < 0) return;
        st[i] = (st[lc(i)] + st[rc(i)]);
        add(par(i));
    }
    void add(int i, int val){
        i+=size-1;
        st[i] += val;
        add(par(i));
    }
    int rsq(int r, int v, int lv, int rv){
        if (lv >= r) return 0;
        if (rv <= r) return st[v];
        int m = (lv+rv)/2;
        return rsq(r, lc(v), lv, m)+rsq(r, rc(v), m, rv);
    }
    int rsq(int i){
        return rsq(i, 0, 0, size);
    }
};

