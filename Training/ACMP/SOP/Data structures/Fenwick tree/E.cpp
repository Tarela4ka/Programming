#define LSOne(S) (S & (-S))
template <typename T>
using v = vector<T>;
using ii = pair<int, int>;
using ic = pair<int, char>;
using vi = v<int>;
using vvi = v<vi>;

const int MOD = 1e9+7;
const int INF = 1e10;
const int maxN = 1e6+2;

class Fenwick{
private:
    int m;
    vi ft;
public:
    Fenwick(int n){ m = n+1; ft.resize(m, 0); }
    int rsq(int i){
        int res = 0;
        for(; i; i-=LSOne(i)) res+=ft[i];
        return res;
    }
    int rsq(int l, int r){
        return rsq(r) - rsq(l-1);
    }
    void update(int i, int v){
        for(; i <= m; i+=LSOne(i)) ft[i]+=v;
    }
    int select(int k){
        int low = 0, high = m+1;
        while(high - low > 1){
            int mid = (low + high)/2;
            if (rsq(mid, maxN) < k) high = mid;
            else low = mid;
        }
        return low;
    }
};

signed main(){
    ios::sync_with_stdio(false); 
    cin.tie(NULL); cout.tie(NULL);
    #ifdef DEBUG
    clock_t timeStart,timeEnd;
    timeStart=clock();
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);
    #endif
    
    int q; cin >> q;
    Fenwick ft(maxN);
    while(q--){
        int z, x;
        cin >> z >> x;
        if (z == 1){
            cout << ft.rsq(x, maxN) << endl;
            ft.update(x, 1);
        }
        if (z == 2){
            x++;
            // cout << x << " " << ft.select(x) << endl;;
            ft.update(ft.select(x), -1);
        }
    }


    #ifdef DEBUG
    timeEnd=clock();
    float diff (timeEnd-timeStart);
    cout << '\n' << diff << "ms\n";
    #endif
    return 0;
}
