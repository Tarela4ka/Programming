#define LSOne(S) (S & (-S))
template <typename T>
using v = vector<T>;
using ii = pair<int, int>;
using ic = pair<int, char>;
using vi = v<int>;
using vvi = v<vi>;

const int MOD = 1e9+7;
const int INF = 1e10;

class Fenwick{
private:
    vvi ft;
    int n, m;
public:
    Fenwick(int y, int x){
        n = y+1; m = x+1;
        ft.resize(n, vi(m, 0)); 
    }
    int rsq(int x, int y){
        int res = 0;
        for(int i = y; i > 0; i -= LSOne(i)){
            for(int j = x; j > 0; j -=LSOne(j)){
                res += ft[i][j];
            }
        }
        return res;
    }
    int rsq(int x1, int y1, int x2, int y2){
        return rsq(x2, y2)-rsq(x2, y1-1)-rsq(x1-1, y2)+rsq(x1-1, y1-1);
    }
    void update(int x, int y){
        for(int i = y; i < n; i += LSOne(i))
            for(int j = x; j < m; j += LSOne(j))
                ft[i][j]++;
    }
};

