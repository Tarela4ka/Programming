signed main(){
    ios::sync_with_stdio(false); 
    cin.tie(NULL); cout.tie(NULL);
    #ifdef DEBUG
    clock_t t1,t2;
    t1=clock();
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);
    #endif

    int t; cin >> t;
    while(t--){
        int x, y; cin >> x >> y;
        int A = 0, B = 0, C = 0;
        if (x < 0){
            if (y < 0) {
                B = -x;
                C = B - y;
            }
            if (y >= 0){
                if (-x > y){
                    B = -x;
                    C = B-y;
                }else{
                    B = y;
                    A = y+x;
                }
            } 
        }
        if (x >= 0){
            if (y < 0){
                A = x; C = -y;
            }else{
                B = y;
                A = B+x;
            }
        }
        cout << A << " " << B << " " << C << "\n";
    }

    #ifdef DEBUG
    t2=clock();
    float diff ((float)t2-(float)t1);
    cout << '\n' << diff << "ms\n";
    #endif
    return 0;
}
